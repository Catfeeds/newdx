<div class="f_r w560">
<p>“雪花勇闯天涯”系列活动是目前国内规模最大、区域最广、持续时间最长的原创性户外极限、探险类品牌公益活动，至今已成功举办了9年。<span>分别是：2005年探索雅鲁藏布大峡谷；2006年探源长江之旅；2007年远征国境线；2008年极地探索；2009年挑战乔戈里；2010年共攀长征之巅；2011年穿越可可西里；2012年冲破雪线；2013年翻越喜马拉雅山。</span>现已成为户外极限运动爱好者的饕餮盛宴，形成了“积极、进取、挑战”的勇闯精神和关爱自然的公益理念。</p>
<p>2014年，“雪花勇闯天涯”挑战未登峰，我们将带领全国勇闯者挑战未登峰，“未登峰”就是要对从来无人登顶的山峰进行挑战性开拓性的攀登。“未登峰”不仅意味着人类从未登顶的山峰，更象征着人类尚未完成的超越。挑战未知从而突破极限，对于所有热爱攀登的户外运动爱好者来说，无疑是一次令人期待的终极体验。</p>
<p>目前，全国选拔正在火热进行中。我们陕西区域将于2014年7月14日~8月17日期间开展2014年雪花勇闯天涯“挑战未登峰”陕西选拔赛。选拔赛分为初赛和复赛两个阶段。初赛胜出的前30名选手参加复赛，复赛的冠亚军则代表陕西参加全国的勇闯天涯“挑战未登峰”决赛活动。</p>
