<p class="z">&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;世界最顶尖露营装备，史上最浪漫出行计划，对波澜不惊的生活说不，转身拥抱绚烂的露营时光！2014年度KingCamp露营体验家大型体验活动，换种方式享户外，带上全家去露营！<br>
&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;由自驾露营装备知名品牌KingCamp倾力打造的"2014年度KingCamp露营体验家"大型体验活动近日正式启动，面向全国户外家庭发出招募令，邀请优胜家庭体验"舒适、轻松、分享、环保"的露营生活！<br>
&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;"2014年度KingCamp露营体验家"大型体验活动将通过四期选拔活动，在全国范围内甄选出约50名露营经验丰富且对家庭户外理念拥有独特解读的佼佼者，赋予其"KingCamp露营体验家"的荣誉称号，用顶尖露营装备助其开始美好露营生活！<br>
&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;"2014年度KingCamp露营体验家"由露营体验师活动升级而来，是KingCamp连续举办3年的大型体验活动，KingCamp希望通过"换种方式去户外，带上全家去露营"的方式推动全家露营生活方式的盛行，户外绝不应局限于个体对自然的征服和探险以及对自我极限的挑战，而更应该是与家人一起到户外去，共同感受、相互分享户外运动所带来的快乐体验并把这种愉悦的心境传递给周围的朋友，去户外&#8226;学会爱！</p>
<p>"KingCamp2014年度露营体验家"荣誉证书；<br>KingCamp家庭露营装备1件的免费体验资格；<br>
2014年组织出行活动时，有机会免费申请KingCamp出行小礼包（每月限10份），详细申请流程请咨询楼主，也可直接发送邮件至以下邮箱：web@kingcamp.com.cn。<br>年底将从所有露营体验家中评选出1名"最优秀的露营体验家"，获赠价值8000元的KingCamp家庭露营大礼包。</p>
<p>露营体验家评选标准：<br>1.基本标准：户外活动经历；往期评测报告质量；8264网站活跃度；所提交的户外出行计划；<br>
2.分数来源：以上四项标准各占25%；<br>3.加分项：认同KingCamp的品牌理念，并对家庭户外有自己的理解。<br>
最优秀露营体验家评选标准：<br>1.基本资格：按时高质量完成产品评测任务，评测文章文笔流畅，图片清晰，评测专业客观，浏览量和回复量较高，并对文章进行及时维护、与读者进行良好互动；<br>
2.分数来源：好友评分与主办方评分各占比50%；<br>3.加分项：体验活动与评测文章体现家庭户外的主旨和健康积极的户外理念。</p>
            
<p>KingCamp，家庭户外的倡导者，户外自驾露营装备知名品牌。<br>自2002年创立以来，KingCamp始终致力于向全世界推广户外生活方式，以其"舒适、轻松、分享、环保"的户外理念、独特的设计、专业的品质和优质的服务赢得全世界户外爱好者的青睐，产品畅销全球30多个国家。<br>
KingCamp的产品涵盖露营、徒步、旅行、休闲四个系列近千个品种。无论从产品设计、面料选择还是质量把控上，KingCamp品牌均严格执行国内外先进标准。<br>KingCamp不仅是一个品牌，更代表一种健康、先进的生活方式，引导人们轻松舒适地享受户外，贴近自然。</p>
