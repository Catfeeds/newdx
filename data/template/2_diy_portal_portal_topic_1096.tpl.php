                <div class="midleftcon1">1923年，世界顶级户外运动鞋品牌―LOWA在德国创立。并在1945年开始扩大生产规模，覆盖从登山鞋、滑雪靴到运动凉鞋等各种运动鞋类。<br>依托其80余年的专业制鞋经验，<a href="http://www.8264.com/29566.html"><img src="http://static.8264.com/oldcms/moban/zt/lowa85/images/lowaboss.jpg" width="174" height="242" border="0" align="right"/></a>LOWA已成为德国国家制鞋标准，并以此为全球提供真正的高品质户外运动鞋。对每个细节的苛求，保证了每双LOWA鞋的卓越品质，更成为最舒适安全的高品质户外运动鞋。<br>今天的LOWA凭借其顶级的材质；配合世界级专业Viabram鞋底；以及十余项专利技术；并在欧洲通过工匠们的精心手工打造，已经成为行业的领头羊，更是全世界户外鞋中的“劳斯莱斯”。<br>
2005年LOWA正式进入中国销售以来，顶级的制鞋材质和德国传统的严谨
制作工艺很快得到国内消费者的认可，短短两年，已成为国内户外鞋销售的
主力军，占领了市场的领先地位。<br>来自德国的LOWA将为你提供包括极限登山，远足，郊游、城市旅游等户外运动所需要的全方位户外
