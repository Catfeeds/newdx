            <P>探路者从2004年开始关注可可西里生态和可可西里的标志性动物藏羚羊的生存环境，并于2006年发起寻梦可可西里的大型活动。活动引起强烈的社会反响，当年可可西里自然保护区管理局就收到一万余张探路者产品附带着的保护可可西里的明信片。探路者公司也向可可西里自然保护区捐献了10万元和60多套装备。</P>
