                  <div class="rightcon2">　　卡鲁雄峰，海拔6674米，地处拉轨冈日山脉，东经90.3度，北纬28.8度，位于西藏自治区日喀则地区江孜县和山南地区浪卡子县交界处。<br>

　　卡鲁雄峰，被当地人尊称为"智慧女神"。从顶峰眺望，卡鲁雄峰周围有6座7000米的山峰，有名的山峰是宁金抗沙（7206米），念青唐古拉（7160米），穷姆冈日，卓姆拉日，加热卡，库拉冈日。20余座6000米的山峰嘎玛（6484米），苯堆（6085米）等山峰。 还有两个美丽的湖泊。 羊卓雍错和普莫雍错是西藏四大神湖中的其中两个湖泊，属于内陆淡水湖泊类型，特别是两大神湖之间， 有众多寺庙， 每年都有大批的虔诚信徒前去敬香拜佛。&nbsp;<a href="/32695.html" target="_blank" style="color:#FF0000">详细<<</a></div>
