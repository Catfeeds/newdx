<h3>2015再度携手奥索卡 相聚白河</h3>
<p>白河攀岩基金用于推广普及攀岩运动，为广大攀岩爱好者开发自然攀岩场地。今年是白河攀岩基金成立十五周年，为此白河基金携手奥索卡并联合中国登山协会、旗云探险、与六月十三日至十四日在白河五道岭新岩区举办白河攀岩节。届时大会将招募志愿者进行攀岩场地环境清理、组织参与者开展趣味攀岩赛，并为初学者和渴望提高攀登技能的爱好者举办公开攀登课程。同时，在十四日晚会有一场盛大的白河攀岩基金晚会，啤酒、烧烤、音乐已经准备就绪，只等广大攀岩者的到来。</p>
