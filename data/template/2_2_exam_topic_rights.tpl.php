                if(data.return_state == '1'){
                    rm_error("tips", "yzm");
                    up_info(address, phone, data.vcode);
                }else if(data.return_state == '-1'){
                    r_error("验证码错误,请仔细查看短信", 'tips', 'yzm');
                    return false;
                }else if(data.return_state == '-2'){
                    r_error("验证码已失效，请重新获取", 'tips', 'yzm');
                    return false;
                }else{
                    r_error("非法参数", 'tips', 'yzm');
                    return false;
                }
            }
        });
    }

    function up_info(address, phone, vcode){
        if(address == '' || phone == ''){
            alert("非法参数！");
            return false;
        }

        //登录
        $.ajax({
            type: 'POST',
            url: '/exam.php?ctl=topic&act=up_info',
            async:false,
            data: {address:address, phone:phone, vcode:vcode},
            dataType: 'json',
            success:function(data){
                if(data.flag == 1){
                    jQuery(".login").hide();
                    jQuery(".page").hide();
                    alert('完善资料成功');
                }else if(data.flag == -1){
                    alert('非法来源');
                }else{
                    alert('非法用户');
                }
            }
        });
    }

    function validate() {
        var address = jQuery("#address").val();
        address = jQuery.trim(address);
        if(address.match(/<|"/ig)) {
            r_error("地址包含敏感字符", 'tips', 'uname');
            return false;
        }

        rm_error("tips", "uname");

        if(checkMobileState()){
            checkYzCode();
        }

        return false;
    }


    function r_error(msg, tipclass, errorclass){
        jQuery("."+tipclass).html("<i class='icon-mark'></i>"+msg).show();
        jQuery("."+errorclass).addClass("error");
    }
    function rm_error(tipclass, errorclass){
        jQuery("."+tipclass).html("").hide();
        jQuery("."+errorclass).removeClass("error");
    }
