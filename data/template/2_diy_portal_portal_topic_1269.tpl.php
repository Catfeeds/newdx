                    <div class="left_mid1"><b>参赛方式：</b><br>所有参赛者将作品发至8264论坛中的“户外美食版块”所有作品均在标题前面加上【第四届火枫美食节】，在规定时间内上传作品的制作图片和制作方法。也可同时将作品在驴友录的日志中发布，并邀请好友点评，邀请好友越多，好评越多获奖几率越大。<br>8264将随时收录作品并在专题中进行统一刊登。<br><b>评选标准：</b><br>1、凡参赛作品都可以参加评选。<br>
2、图片数量多，照片清晰，制作过程详细获奖几率大。<br>
3、作品在论坛及驴友录中的回帖、点评及踩脚印数量越多，获奖几率大。<br>
4、使用火枫或黑鹿产品的参赛者获奖机率大。<br>
5、参加过之前任意两届，今年继续参加的获奖几率大。<br>
6、本次活动中提交作品数量越多，获奖几率越大。<br>
7、最终获奖作品由活动组委会根据以上各项标准进行综合评定。<br>评审团组成：8264、火枫公司<br><b>活动时间：</b><br>宣传时间：9月1日――9月10日<br>
征稿时间：9月11号-10月20号<br>
