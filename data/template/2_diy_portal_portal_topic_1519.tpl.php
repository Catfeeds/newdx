<div class="yan-tit-wrap mt30">
<h3 class="title">活动简介</h3>
</div>
<div class="act-intro-word mt30">
<p>2012年夏季，KAILAS “寻岩中国”活动在阳朔首次启动，各地的顶尖攀登高手组成了开线团队齐聚“寻岩中国”。一站站下来，他们在发现中国各地鲜为人知而又最优质的岩壁资源的同时，也为当地岩友们开出了许多有趣的新线路，吸引了众多热爱攀登的人们一起分享岩上乐趣。</p>
<p>那些藏匿于山野间的岩壁，是世间最具魅力的景观，是大自然最有灵性的居所，更是攀岩者最向往的圣地。而“寻岩中国”，则是这些灵动峻美的岩壁的呈现者。今年，寻岩团队更加壮大，由KAILAS攀登队员谢卫成、赵忠军牵头，更多的国内高手如张晓柳、邱江、张勇、苏峰、Marcos、李兰、裂缝加入其中，一齐再次出发，在全国各地寻觅发掘新人、新岩、新体验，开启960万平方公里上的新一轮寻岩之旅。</p>
<p>2014“寻岩中国”团队综合考虑了各地岩壁资源情况与当地岩友的实际攀爬需求以后，确定四站活动的地点分别为：北京房山、辽宁鞍山、本溪、成都及乐山、丽江石鼓。新站点新体验，各站资料让我们抢鲜一睹为快：</p>
