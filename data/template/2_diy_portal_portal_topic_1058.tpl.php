  <div style="width:708px;padding:20px;font-size:14px; line-height:155%;color:#333; text-indent:2em; text-align:left;"> <STRONG>前言：</STRONG>从1960年诞生至今，西藏登山队在90年代已成为世界上最具实力的队伍之一。西藏登山运动员们也都是具有高素质、高水平、经验丰富的运动员。为了国家的荣誉，向世界登山界展示中国改革开放以来中华儿女崭新的精神面貌，西藏登山队已完成世界上所有14座海拔8000米以上的高峰挑战。于是1992年4月16日经西藏自治区人民政府批准，中国西藏攀登世界14座8000米以上高峰探险队正式成立。从1993年开始，他们在喜马拉雅和喀拉昆仑两条山脉上，一次次将五星红旗插在世界上著名的高山之颠。又一次次向国内外证明了西藏登山队作为世界登山巨人的实力。2007年7月12日12时20分攀登上最后一座8000米高山 迦舒布鲁姆Ⅰ峰 ，展开鲜艳的五星红旗，激动地互相拥抱，庆祝登顶世界上全部14座高峰的完满大结局。


    <P></P>
<P>为了这一天的到来，这支世界上唯一一支以团体形式攀登14座高峰的登山队，在喜马拉雅和喀喇昆仑山脉的冰峰雪岭间奋斗了14年。他们把人生最美好的青春年华留在雪山深处，最终实现了中国登山人的梦想。这套名信片就是为了纪念西藏登山队攀登14座8000米山峰的一个缩影。</P></div></DIV>
