                                      <TD>2004年6月16日，中国登山爱好者“十一郎”前往位于俄罗斯，攀登位于高加索山脉的欧洲最<BR>
                                        高峰“厄尔布鲁士Elbrus山”（海拔5642米）。这是7年以来，第一次有中国人踏足厄尔布鲁士山区。也是中国业余登山爱好者首次攀登欧洲最高峰。经过2天的侦察拉练和3天的适应攀登，6月22日当地时间11:10（北京时间15:10），“十一郎”成功登顶厄尔布鲁士。在冲顶过程中，曾在5000米以上遭遇小型暴风雪，风力达到七级。此图片专集记录了攀登过程和该山所处地区的景色</SPAN><BR></TD>
