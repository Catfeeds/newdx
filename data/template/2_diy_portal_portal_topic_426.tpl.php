                            <TD class=style2>　　欧洲最高峰厄尔布鲁士（Elbrus）山，海拔5642米，位于北纬43度21分、东经42度26分，在欧、亚两洲交界处的俄罗斯和格鲁吉亚边界的高加索地区。该山属于高加索山系的大高加索山脉的博科沃伊支脉。厄尔布鲁士山的北偏东65公里是俄罗斯Kislovodsk城，其南方20公里是格鲁吉亚的Caucasus区。厄尔布鲁士山的雪线，北坡在海拔3200米，南坡则在3500米。周围有77大小条冰川，总面积达140平方公里。冰川末端溢出的溶水，形成了周围数以百计的溪流。冬季结束后，雪线以上的积雪厚度通常在30～60公分，有时甚至达到3米深。厄尔布鲁士山附近有Baksan山谷、Donguzorun山谷、Yusengi山谷、Adylsu山谷、Irik山谷、Adyrsu山谷、Kyrtyk山谷、SyItransu山谷等主要山谷。高加索地区气候差异明显：大高加索山以北的北高加索属温带大陆性气候；以南的外高加索属亚热带气候。厄尔布鲁士山附近的天气以东西峰鞍部为分界，其北偏东是温带大陆性气候带来的寒冷空气，其南偏西是黑海亚热带气候带来的暖湿气流。厄尔布鲁士山地区的气候主要受西来气流的控制。厄尔布鲁士山南侧，雪线以下的年降水量为550～650毫米，雪线以上达到950 ～1050毫米，但主要集中在秋、冬季。春、夏季的天气以温暖、晴朗为主，但会有雷暴，尤其是夏季初比较频繁。海拔2000米以上，冬季在4月份才结束。为了避开雪崩和雷暴，厄尔布鲁士山的攀登季节通常是6月到8月。厄尔布鲁士山传统攀登路线，位于山体的南侧。线路特点比较平缓，技术难度不高。全程不需要修路绳；部分线路两侧是裂缝区；部分区域有结冰情况，需要特别注意。攀登厄尔布鲁士山需要克服的2大障碍是漫长的路线和天气的变化。</SPAN></TD>
