    <td align="left" style="padding-left:10px; padding-right:10px;">　　越野跑，在野外自然环境中通过各种地形及障碍物的跑步。越野跑是最直接、单纯的一种户外运动赛事，无论是初学者还是专业选手，只需要一双高质量的跑鞋，就可以参加越野跑，就可以在挑战自我的过程中体验置身户外的欢愉。<br>

　　多年来，美国领先户外鞋品牌VASQUE在北美赞助了一系列著名的高水准越野跑赛事，2008年联手美国著名面料生产商戈尔公司举办的第二届北美越野跑巡回赛（North American Trail Tour，NATT），包括了美国和加拿大的112条经典越野跑路线。<br>
    　　在中国大陆，VASQUE品牌及其代理商深圳市喜马拉雅贸易有限公司已经连续两年冠名赞助了杭州西湖跑山赛。2008年，VASQUE品牌联合戈尔工业品贸易（上海）有限公司，将在大陆三个城市举办2008“威斯―戈尔特斯”杯越野跑分站赛，让国内的户外运动爱好者和长跑爱好者体验到预约越野跑运动带来的山野美景、清新空气和比赛的紧张刺激。</td>
