              <p>2012年10月28日 D1 出发抵达福贡县水里底村景：主要以怒江峡谷风景，傈傈族山寨人文，亚热带高山原始丛林为主</p>
              <p>2012年10月29日 D2 福贡县阿底玛底高山湖景：亚热带高山原始丛林为主，高山风景，高山湖(第四纪冰川遗址）</p>
              <p>2012年10月30日 D3 维西县新化高山湖视行程进度，备选多徒步新化湖北部4个高山湖（景同上）</p>
              <p>2012年10月31日 D4 维西县新化村景：主要以澜沧江干热河谷，傈傈族山寨人文为主</p>
              <p>2012年11月1日 D5 维西县维登乡政府景：主要以澜沧江干热河谷、乡镇为主</p>
              <p>2012年11月2日 D6 维西县全天上坡路</p>
              <p>2012年11月3日 D7 锅底塘全天上、下缓坡；景：红土高原旱地、农庄</p>
              <p>2012年11月4日 D8 新主村（鲁甸乡）全天下缓坡</p>
              <p>2012年11月5日 D9 雾露村全天下缓坡（或直接到达巨甸镇）</p>
              <p>2012年11月6日 D10 丽江玉龙县巨甸镇全天下缓坡（或早起看金沙江日出）</p>
