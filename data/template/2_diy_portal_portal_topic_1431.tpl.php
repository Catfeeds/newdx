              <p>  &nbsp;&nbsp;&nbsp;&nbsp;著名探险家金飞豹将和思凯乐品牌创始人曾花以及生态学家、地质学家以及招募选出的"梦想行者"一起，对三江并流地区进行徒步科考探险活动。此次探险将全面收集该地区自然资源、人文资源资料的第一手资料，为今后当地政府保护和开发三江并流地区提供重要的参考。</p>
