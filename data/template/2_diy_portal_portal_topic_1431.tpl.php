  ● 关注三江自然生态环境,更为有效的保护和合理开发  
     三江并流地区<br />
  ● 关注三江人文生态环境，给少数民族的孩子们带去
     梦想的希望<br />
  ● 激励更多的人走向户外、乐享户外</p>
