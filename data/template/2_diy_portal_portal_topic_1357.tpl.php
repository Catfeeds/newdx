<h2>
<a href="" class="r_2" title="品牌故事"></a>
<span class="r_2_r"></span>
<div class="clear"></div>
</h2>
<div class="r_2_bottom"></div>
<div class="list box-border-2">
<div class="brand-story">
<p>奥索卡代表着最纯粹的户外精神，奥索卡品牌对户外运动的热忱及对大自然和谐相处的理念来源于纯净自然的山脉。</p>
<p>瑞士户外运动爱好者 Hans Shallenberger 在1996年创立奥索卡品牌，并立即投入对中国高海拔攀登探险的支持。今天，奥索卡的足迹已遍布世界，从90&deg;s 的南极点到8848m的珠穆朗玛之巅，奥索卡户外装备伴随探险家的脚步经历了各种严酷环境，也见证了一个又一个成功的时刻。</p>
<p>凭着对户外运动的热爱和对优越品质的坚持，奥索卡将攀登探险领域的丰富经验运用到户外产品中，设计研发的四大系列300余种户外装备，不仅满足各种极端环境的探险要求，也适合各类户外运动。对高品质的不断追求，和对户外与自然的理解，使奥索卡品牌始终保持创新，使更多人发现和享受户外运动的乐趣。</p>
<p>奥索卡品牌为促进户外运动持续发展不断付出努力，西藏登山学校、CMDI中国登山高级人才培训班的建立，使世界前沿理念融入中国攀登运动，持续进取；对山峰资源与环境的维护，亦体现品牌长远发展的信心。</p>
<p>无论时代如何变迁，人们对自然世界的向往不会改变。我们坚信这样的理念-所有奥索卡产品的使用者都是奥索卡的伙伴，我们将一如既往地为热爱探索、对大自然充满热情的人们提供最出色的装备。</p>
