                	<h1>CMDI是中国阿式攀登的积极倡导者和实施者</h1>
                    <div class="clearfix" style="margin-bottom:10px;"><img src="http://static.8264.com/oldcms/moban/zt/20121211_ask/images/wen.png" class="fl"/><p class="fl">CMDI对于中国阿式攀登发展起到了什么样的作用呢？
</p></div>
                    <div class="clearfix" style="margin-bottom:10px;"><img src="http://static.8264.com/oldcms/moban/zt/20121211_ask/images/da.png" class="fl"/><p class="fl">阿式攀登需要攀登者在没有协作、不依赖路绳的情况下自主攀登，这需要攀登者自身必须具备一定的登山知识和技术才能安全地攀登。CMDI创建的目的就是在中国推广阿式攀登形式，我们开设的课程就是教给阿式攀登者知识和技术，让他们能够自主的攀登；同时我们还培养阿式攀登向导，让这些向导去为更多进行阿式攀登的人服务，让他们在安全的前提下享受自己攀登的乐趣。CMDI是中国阿式攀登的积极倡导者和实施者。
</p></div>
                </div>               
            <div class="fr wd" style="border:0;">
                	<h1>阿式攀登只有一种，没有中外之分</h1>
                    <div class="clearfix" style="margin-bottom:10px;"><img src="http://static.8264.com/oldcms/moban/zt/20121211_ask/images/wen.png" class="fl"/><p class="fl">通过这次混合编组的攀登，CMDI从法国登山者身上看到的阿式攀登有什么特点，与中国阿式在理念上有什 
么不同之外吗？
</p></div>
                    <div class="clearfix" style="margin-bottom:10px;"><img src="http://static.8264.com/oldcms/moban/zt/20121211_ask/images/da.png" class="fl"/>
                    <p class="fl">我想阿式攀登只有一种，没有中外之分，我们还处在成长和学习阶段，这次混合编组的目的就是想通过一起攀登学习到法国登山者的先进技术和理念。阿式攀登提倡轻装、快速，不依赖协作和路绳，一切都靠攀登者自己的自主攀登，在山上的时间短而减少攀登的风险。这次法国登山者确实体现出了阿式攀登的全部特点，他们都有很强的体力，攀登虽不是动作很快，但节奏稳定，技术熟练，尽量使用简洁的方式，不会浪费时间，这样攀登的效率很高；我们的阿妣峰攀登，山脊路线难度D，高差700米，只用了12个半小时上下，这在我们中国攀登者中还没用出现过这样的成绩。</p></div>
