                	<h1>中法攀登交流 学习先进的攀登技术和理念</h1>
                    <div class="clearfix" style="margin-bottom:10px;"><img src="http://static.8264.com/oldcms/moban/zt/20121211_ask/images/wen.png" class="fl"/><p class="fl">本次中法混合结组攀登是基于什么样的原因促成这样一个攀登计划呢？
</p></div>
                    <div class="clearfix" style="margin-bottom:10px;"><img src="http://static.8264.com/oldcms/moban/zt/20121211_ask/images/da.png" class="fl"/><p class="fl">法国攀登者渴望来中国这个山峰资源极度丰富的国度攀登，中国攀登者想要学习到法国攀登者先进的技术和理念，这就是促成这个计划的原因。2010年高山集团副主席Christian就和他的朋友来中国攀登过，当时我们在四川贡嘎山地区进行攀登，当他看到中国的丰富漂亮的雪山资源之后，就一直酝酿组织法国攀登者来中国和中国攀登者一起攀登。
</p></div>
                </div>               
            <div class="fr wd" style="border:0;">
                	<h1>地理位置和环境条件是选择四座山峰的原因</h1>
                    <div class="clearfix" style="margin-bottom:10px;"><img src="http://static.8264.com/oldcms/moban/zt/20121211_ask/images/wen.png" class="fl"/><p class="fl">这次攀登为什么要选择这四座山峰，有什么代表性和意义吗？</p></div>
                    <div class="clearfix" style="margin-bottom:10px;"><img src="http://static.8264.com/oldcms/moban/zt/20121211_ask/images/da.png" class="fl"/><p class="fl">这四座山峰在四川的雪山中都是具有一定难度的山峰，无论从外型上还是实际的攀登中都具有一定的挑战性，这是选择这四座山峰的初衷。但攀登的可变因素太多，这次的攀登目的并不是一定要完成这几座山峰的登顶，而是到中国来和中国的攀登者一起攀登才是重要的。</p></div>
              </div>
            </div>
            <div class="zf clearfix" style="margin-bottom:20px;">
            <div class="fl wd">
                	<h1>中国的阿式攀登起步晚但发展迅速</h1>
                    <div class="clearfix" style="margin-bottom:10px;"><img src="http://static.8264.com/oldcms/moban/zt/20121211_ask/images/wen.png" class="fl"/><p class="fl">法国登山者通过CMDI是如何看待中国的阿式攀登发展的呢？</p></div>
                    <div class="clearfix" style="margin-bottom:10px;"><img src="http://static.8264.com/oldcms/moban/zt/20121211_ask/images/da.png" class="fl"/><p class="fl">法国登山者很多还都不了解中国阿式攀登，在法国拥有很大的阿式攀登群体，在那里的攀登形式只有阿式攀登。这次中法交流攀登，在他们眼里，我们的阿式攀登才开始不久，但通过CMDI也让他们看到了中国阿式攀登的成长，虽然与他们还有很长的距离，但发展是迅速的，从2006年创建CMDI到目前已经有越来越多的攀登者对阿式攀登产生了极大的兴趣。</p></div>
                </div>                
            <div class="fr wd" style="border:0;">
                	<h1>中国拥有辽阔的山峰资源对阿式攀登有大用武之地</h1>
                    <div class="clearfix" style="margin-bottom:10px;"><img src="http://static.8264.com/oldcms/moban/zt/20121211_ask/images/wen.png" class="fl"/><p class="fl">CMDI重点致力于中国阿式攀登的培训，那么CMDI怎么看待中国的阿式攀登资源呢？</p></div>
                    <div class="clearfix" style="margin-bottom:10px;"><img src="http://static.8264.com/oldcms/moban/zt/20121211_ask/images/da.png" class="fl"/><p class="fl">阿式攀登发源于欧洲阿尔卑斯山脉，是现代登山的代表攀登形式。中国拥有辽阔的山峰资源，对阿式攀登而言无疑是广阔的用武之地，在中国的西部省份，尤其是四川、云南分布着大量的5000、6000、甚至7000米级的雪山，这些都是可以在短时间周期完成攀登的阿式攀登资源；只是目前我们的阿式攀登群体还不够庞大，受限于交通条件的困难，很多山峰都还没有去攀登和开发，不过这也是我们拥有的巨大潜力资源。</p></div>
              </div>
            </div>
            <div class="zf clearfix" style="margin-bottom:20px;">
            <div class="fl wd">
                	<h1>攀登队员的条件从技术上说比较先进和系统</h1>
                    <div class="clearfix" style="margin-bottom:10px;"><img src="http://static.8264.com/oldcms/moban/zt/20121211_ask/images/wen.png" class="fl"/><p class="fl">这次攀登中，中国队员的选择有哪些条件和要求吗，他们都有哪些攀登经历呢？</p></div>
                    <div class="clearfix" style="margin-bottom:10px;"><img src="http://static.8264.com/oldcms/moban/zt/20121211_ask/images/da.png" class="fl"/><p class="fl">这次的中国队员都是毕业于CMDI培训班，接受过从法国引进的攀登体系的培训，在技术层面上来说是比较先进和系统的，6名队员中5名都去法国进行过攀登训练，而其中一些队员都有很好的攀登经历和成绩，完成过一些有难度的山峰攀登，比如：四姑娘幺妹峰，布达拉峰等。</p></div>
                </div>                
            <div class="fr wd" style="border:0;">
                	<h1>利用中国的攀登资源发起更多的攀登交流活动</h1>
                    <div class="clearfix" style="margin-bottom:10px;"><img src="http://static.8264.com/oldcms/moban/zt/20121211_ask/images/wen.png" class="fl"/><p class="fl">今后还会发起类似这样的攀登计划吗？</p></div>
                    <div class="clearfix" style="margin-bottom:10px;"><img src="http://static.8264.com/oldcms/moban/zt/20121211_ask/images/da.png" class="fl"/><p class="fl">答案是肯定的，中国的阿式攀登正在不断的普及和发展当中，而我们的攀登资源又是独一无二的，我们需要与更多的世界攀登者一起攀登、交流，世界的攀登者也越来越喜欢到中国来攀登，所以，今后我们还会组织发起更多的类似的交流攀登活动，学习攀登发达国家的经验和理念，更快让我们的阿式攀登成长起来并与世界接轨。</p></div>
              </div>
            </div>
            <div class="zf clearfix" style="margin-bottom:20px;">
            <div class="fl wd">
           	  <h1>发展国内攀登事业；学习，交流是很好的途径</h1>
                    <div class="clearfix" style="margin-bottom:10px;"><img src="http://static.8264.com/oldcms/moban/zt/20121211_ask/images/wen.png" class="fl"/><p class="fl">以这样的方式做为中外攀登交流切口，有哪些意义所在吗？</p></div>
                    <div class="clearfix" style="margin-bottom:10px;"><img src="http://static.8264.com/oldcms/moban/zt/20121211_ask/images/da.png" class="fl"/><p class="fl">欧美等发达国家在现代攀登方面的发展历史已经超过200年，具有非常深厚的积累和沉淀，无论从攀登技术方面还是理念上，都是非常先进的，所以在一些极限攀登上，也有难以想像的成绩；我们的攀登起步较晚，而要想较快的发展起来，学习、交流是一条很好的捷径，如果我们去发达国家学习固然好，但成本较高，而且我国的山峰资源条件与国外的也有很大差异，如果像这样让国外攀登者来中国和我们一起攀登交流，不仅可以在我国山峰条件下学习到经验、技术，还可以更好的开发我们的山峰资源。</p></div>
              </div>              
            <div class="fr wd" style="border:0;">
                	<h1>CMDI会组织更多的培训班和攀登活动</h1>
                    <div class="clearfix" style="margin-bottom:10px;"><img src="http://static.8264.com/oldcms/moban/zt/20121211_ask/images/wen.png" class="fl"/><p class="fl">号称中国攀登界的"黄埔军校"，明年会有哪些动态和发展吗？</p></div>
                    <div class="clearfix" style="margin-bottom:10px;"><img src="http://static.8264.com/oldcms/moban/zt/20121211_ask/images/da.png" class="fl"/><p class="fl">从2006年开始到现在，CMDI已经举办了超过七个班的培训，培养了近40名攀登者和向导；随着CMDI培训在中国的进一步发展，明年会组织更多的培训班和攀登活动，以及攀登发展活动；除了正常的培训之外，我们将会邀请世界知名的攀登者和攀登向导到中国来进行交流、攀登、讲课；可能会邀请到前世界杯攀岩冠军、Petzl赞助运动员Daniel Dulac来中国与CMDI一起开发自然岩壁攀岩线路。并将继续与法国高山集团（GHM）合作组织法国高山向导和CMDI学员的结组攀登。在中国的西部省份，尤其是四川、云南分布着大量的5000、6000、甚至7000米级的雪山，这些都是可以在短时间周期完成攀登的阿式攀登资源；只是目前我们的阿式攀登群体还不够庞大，受限于交通条件的困难，很多山峰都还没有去攀登和开发，不过这也是我们拥有的巨大潜力资源。
</p></div>
