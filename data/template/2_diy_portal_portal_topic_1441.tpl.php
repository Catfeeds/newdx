            <p>11月一支由<a href="http://www.ozarkgear.com/" target="_blank">奥索卡</a>赞助的由<a href="http://www.cmdi-china.com/zh-hans" target="_blank">CMDI</a>教练及学员所组成的攀登团队与法国高山集团在国内联合进行一项名为"2012中法攀登交流行动"的攀登活动。19名中法攀登者以<a href="http://www.8264.com/viewnews-12632-page-1.html" target="_blank">"阿尔卑斯"方式</a>进行三周的攀登。</p>
