<h3 class="h3bigtitle">2014 牛人牛队&nbsp;&nbsp;<span class="red">第四季</span>召集</h3>
<span class="red">在每季召集帖中选出15个队伍，每个队员可获得BLACK YAK T恤一件+环保袋，领队可额外获得鞋子、速干T恤。</span>
<p>
评选依据：<br/>
1. 线路的新颖性、可执行性、以往作业的质量、论坛的活跃度。<br/>
2. 2013年特别好评队伍可直接入选，出行时间需提前与8264沟通。<br/>
5.28 公布结果     队伍数量：15支队伍<br/>
参与形式：跟帖提交5.31到7.15出行计划（时间、人数、出行计划和线路）。<br/>
活动要求：<br/>
1. 每支出行队伍20人最佳（特殊情况除外）。<br/>
2. 根据提交的计划出行，不能随便更改线路（如遇特殊情况更改路线，必须提前与8264沟通）。<br/>
3. 出行结束后，15天之内在地方版发布作业贴，图片必须30张以上，其中带有明显品牌元素的照片至少15张。<br/>
4. 选择至少15张优秀照片原片（含品牌元素，2MB以上）发至指定邮箱（邮箱地址会直接提供给获选领队，拍摄效果为评选依据之一）。<br/>
5. 在收到赞助产品后和出行中各发布一条微博，并@布来亚克微博。<br/>
6. 作业帖子标题格式：2014布来亚克（BLACK YAK）牛人牛队+地区+队伍名称+线路。<br/>
7. 如收到赞助产品一个月后仍未出行，须退回所有产品；如出行后未提交作业的将拉入黑名单。<br/>
8. BLACK YAK 有权使用获选队伍活动相关的图片用于品牌推广宣传。<br/>
</p>
