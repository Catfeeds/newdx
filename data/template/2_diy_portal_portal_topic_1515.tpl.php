<p>BLACKYAK意为黑牦牛，中文音译为布来亚克。1973年创立于韩国，是研究和生产，登山、野营、攀岩等户外服装、装备的国际性专业户外运动用品品牌。<p>
<p>牦牛是青藏高原特有的物种，生活在自然环境非常恶劣的高海拔地区。具有耐寒、善于负重的特性，是古代藏族神话中的氏族守护神。</p>
<p>在极限的环境中，优秀的户外装备就是生命的保障。因此BLACKYAK，崇尚“己所不欲，勿施于人”的顾客哲学，坚持“只为顾客提供经过极限条件检验的产品”的品质理念，追求产品性能和功能的卓越，追求设计上更贴近使用者在户外活动中的需求。</p>
<p>1998年BLACKYAK进入中国市场以来，一如既往地重视与环境的友好关系，注重与消费者的深度交流，倡导和推广"山野之约"户外生活理念。BLACKYAK每年组织形式不同的与环境保护相结合的活动，使消费者有机会去体验充满新奇的户外旅程和BLACKYAK高品质户外产品的同时树立绿色户外生活理念。</p>
<p>BLACKYAK，犹如古代藏族神话中的氏族守护神，牦牛一样，默默地陪伴在户外玩家的身边，始终守护着每一位户外登山探险爱好者。</p>
