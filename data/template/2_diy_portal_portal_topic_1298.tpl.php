        	<h2>活动简介</h2>
        	　　每个品牌的背后，都有一个故事，著名户外品牌“天石”的背后也有着这样一个故事，我们轻声讲述着一个个执着于社会责任的企业所锻造的品牌灵魂，用她的湛蓝映衬着每一个聆听者善良的心。<br>
　　记得2007年春天的<span class="STYLE7">“温暖有多暖”</span>主题吗？天石品牌赞助云南永宁乡永宁中学全部贫困住校生寝具；记得2008年的<span class="STYLE7">“越冬计划”</span>吗？天石将千余条睡袋陆续捐献给需要他们的贫困山民...一路走来，天石人一直关心着那些需要帮助的人们。<br>　　
<span class="STYLE7">2009年冬天</span>，天石再次走进希望小学，走到那些贫困学生身边，用一条条保暖的睡袋，温暖孩子们的心。天石用一颗虔诚的心，做自己最擅长的事来回馈世界，我们将社会责任刻入天石的灵魂。        </div>
