                  <div class="rightcon3">　　北航凌峰社成立于2001年6月，社团原名是北京航空航天大学攀岩俱乐部，后更名为北航凌峰社，并于2004年11月组建登山队，于2006年4月组建科考队。<br>

　　社团的主要活动包括登山、科考、攀岩和野营穿越四部分，另外每周二四晚九点在南操场组织日常训练（每周两训）。社团每年组织登山队于暑假期间攀登雪山；攀岩活动主要在北航体育场的人工岩壁进行；并定期组队到京郊各自然岩壁训练。社团以两周一次的频率不定期组织野营穿越活动，并于寒假组织冬季雪山模拟训练。&nbsp;&nbsp;&nbsp;&nbsp;<a href="/32662.html" target="_blank" style="color:#FF0000">详细<<</a></div>
