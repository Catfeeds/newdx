                    <div class="rightcon2">　　桑丹康桑峰(Samdain Kangsang)，海拔6590米，地处念青唐古拉山脉中段，东经91.5度，北纬30.9度，位于西藏自治区那曲地区谷露乡境内。“冈底斯山―念青唐古拉山脉”横亘西藏自治区中部，东起昌都地区嘉黎，东南延伸与横断山脉伯舒拉岭相接，西到阿里地区狮泉河，南界印度河上源噶尔藏布及雅鲁藏布江谷地，它将西藏自治区划分为藏北、藏南、藏东南三大地域，是雅鲁藏布江和怒江两条大水系的分水岭。<br>
