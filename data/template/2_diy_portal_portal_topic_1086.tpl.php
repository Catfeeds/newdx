<td width="70%" align="left" valign="top" style=" line-height:1.5; padding: 5px 5px 5px 5px">登山滑雪在英文中叫“Ski mountaineering”，法语中称为‘Ski Alpine’。所谓登山滑雪，就是使用滑雪器材登山雪山并一路滑下来。使用的滑雪器材可以自由选择，既可以是越野滑雪的雪板，也可以是标准的斜坡滑雪板、缆车滑雪板，抑或轻便的北欧式滑雪板、短雪板，甚至是单板都可以。登山滑雪运动是一项在欧美、特别是阿尔卑斯、比利牛斯山区周边国家广泛开展的一项冬季户外运动项目。它的起源已无法考证，大概是从人类开始利用雪橇板在积雪的山区活动就有已掌握这项技巧了。<br><br>						现代登山滑雪竞赛包括垂直攀登，个人越野、团队越野、团体接力赛等项目。垂直攀登要求运动员使用滑雪板在最短的时间内在陡峭的雪坡上向上攀登高差一千米左右的距离。而越野赛则要求运动员利用滑雪板连续攀登和快速滑下，甚至要求运动员携带滑雪装备，利用绳索、冰爪等雪山攀登装备登上陡峭的山峰然后滑雪下降。
