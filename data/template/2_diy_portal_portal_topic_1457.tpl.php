        	<h3 class="title">品牌故事<a name="n2"></a></h3>
            <p>SALEWA，SA意为Saddler（制造马鞍的）、LE意为Leather（皮革）、WA意为Wares（制品）。<br>
1935年，德国马具协会主席Josef Liebhart在慕尼黑创建了SALEWA GmbH公司，主要为当时众多的马术爱好者提供优质耐用的马具用品及纺织产品，并生产了第一个金属管架式背包。随着1952年冬季市场上对滑雪杖的大量需求，SALEWA在运动产品领域内有了突破性的发展。1955年，SALEWA支持了Hermann Huber在科迪勒拉山脉的安第斯探险，从而树立了SALEWA在登山探险中的专业地位。1962年，SALEWA研制出一款超轻的全可调冰爪并由此成为登山探险装备的领导者。1978年，振翅雄鹰正式成为SALEWA品牌的商标。<br>
九十年代，意大利Ober Alp集团为SALEWA严谨、可靠、高品质的德国血统注入了来自意大利管理团队的自由、激情、时尚元素，使SALEWA也成为专业户外功能产品与欧洲流行时尚完美结合的产品代表。2011年，英思柏锐贸易（北京）有限公司将SALEWA产品正式带入中国。经过对亚太市场两年的探索、积淀，2013年3月，SALEWA集团全球唯一合资子公司"SALEWA 中国"宣布成立，这个拥有78年登山探险历史的德意混血品牌开启了布局亚太户外市场的新篇章。</p>
