<p><a href="http://bbs.8264.com/forum.php?mod=viewthread&amp;tid=1059701" style="color:#FFFF00;">徽杭古道</a>：大家好，本人菜驴一头，江南水乡人士，具有以下特点：装备差、经验少、认死理、怕花钱、胆子小、脂肪多等，突然一日决定试下新买的廉价装备，于是乎花了半天时间选择并策划了公认的初级线路3日之旅，悲催的是没有约到好友，更悲催的没有看好天气，结果，我硬生生的把这个提着塑料袋都能穿越的初级线路走成了一个中级线路</p>
<p><a href="http://bbs.8264.com/forum.php?mod=viewthread&amp;tid=999347" style="color:#FFFF00;">神农架</a>：远古时期，神农架林区还是一片汪洋大海，经燕山和喜马拉雅运动逐渐提升成为多级陆地，并形成了神农架群和马槽园群等具有鲜明地方特色的地层。神农架位于我国地势第二阶梯的东部边缘，由大巴山脉东延的余脉组成中高山地貌，区内山体高大，由西南向东北逐渐降低。神农架平均海拔1700米。山峰多在1500米以上，其中海拔3000米以上的山峰有6座，海拔2500米以上山峰20多座，最高峰神农顶海拔3106.2米，成为华中第一峰，神农架因此有“华中屋脊”之称。西南部的石柱河海拔仅398米，为境内最低点，相对高差达2708.2米。</p>
