                    	<h1>比赛项目</h1>
                        <p>冰雪三项（登山滑雪、雪地越野、攀冰）</p>
                        <h1>比赛形式</h1>
                        <p>团体三项赛（团体由四人组成，其中至少要有一名异性）<br>个人三项赛<br>个人单项赛2<br></p>
                        <h1>比赛规则</h1>
                        <p>本次比赛规则根据国际滑雪登山联盟最新国际规则制定，于比赛前一周公布于主办单位网站（万龙滑雪场网站www.wlski.com)。</p>
                        <h1>评比标准</h1>
                        <p>1、团体三项以团队参赛者三项综合成绩加和为准。<br>2、个人三项按三项总和时间评定名次，设男子组和女子组。<br>3、个人单项按时间评定名次，设男子组和女子组。<br>4、每个项目均按时间长短进行评定名次，取前六名。</p>
                        <h1>报名方式</h1>
                        <p>报名时间：2012年1月15日-2012年2月26日中午12：00截止，逾期不予报名<br>报名方式：<br>1.万龙客服中心（现场报名）<br>2.网络报名，报名表 <a href="http://static.8264.com/oldcms/moban/ziliao/xinwen/WangLong.doc" target="_blank" style="color:#036; font-weight:bold;">点击下载</a>   填写完毕后发送至邮箱wlice001@163.com<br>客服报名咨询电话：（0313）4785111，联系人：李小姐<br>网络报名咨询电话：（0313）4613872，联系人：李先生<br>传真：（0313）4617676<br>报名费：<br>①500元/人，1000元/队，报名费均含住宿，报名费汇款或现金均可。<br>②个人或团差旅费用及餐费需自理，赛程所需饮水由主办方提供。<br>参赛资格：<br>①队员须为具有有效证件的合法居民，外籍队员报名时，必须持有护照或签证等有效证件。<br>②身体健康，无任何影响运动本身的不良疾病，如心脏病、冠心病、癫痫等。<br>③参赛队员需了解登山户外运动知识，掌握各比赛项目技术，具备良好的体能。<br>④参赛队员年龄在16岁-50岁（含16岁，即1996年12年31日之前出生）。<br>⑤由飓风团队负责参赛资格审查。<br>报到及离赛规定：<br>①队员报到时间：2012年2月26日全天<br>②报到地点：万龙滑雪场双龙酒店<br>③2012年3月2日下午离开<br>④队员报到时需持有效证件（身份证、护照等），外籍队员必须持护照报到。<br></p>
                        <h1>比赛流程</h1>
                        <p>2012年2月26日下午18:00点，抽签分组，赛前大会。<br>2012年2月27日-2月28日团队赛<br>2012年2月29日-3月1日个人赛<br>2012年3月2日上午双龙酒店会议室召开颁奖大会，现场颁发奖品。<br>备注：团队三项赛、个人三项赛与个人单项赛同时进行颁奖仪式。<br></p>
