<div class="act_intro"> 数星客2011第四届中国萍乡武功山国际帐篷节，以"驴行武功山 狂欢帐篷节"为主题，于9月16日正式启动，历时三天，届时将迎接来自海内外的逾万名户外发烧友、爱
好者前来参加，在武功山金顶景区共同见证万人狂欢的壮观场面。活动内容在传承了第三届国际帐篷节备受追捧的负重穿越赛、户外露营晚会等节目同时，更是开拓出了包括
云中草原"俱"人大PK、"驴鸣"大赛、户外美驴服装秀、原创摄影图片大赛等主题活动，旨在带领驴友们共同度过一个美好难忘的"穿云之旅"。 </div>
