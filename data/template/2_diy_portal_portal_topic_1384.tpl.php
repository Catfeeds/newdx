<div class="intro_cont">
<p>数星客SURETEX依托集团公司十多年积累的丰富经验，秉承&quot;保护原生态文化、做原生态旅行家&quot;的品牌理念，通过自主生产基地专业研发高品质的功能性与舒适性相结合的冲锋衣、滑雪服、速干衣、帐篷、睡袋、登山鞋包等户外用品。 </p>
<p>2010年，SURETEX数星客与萍乡武功山强强联手，正式建立战略合作伙伴关系。2010年9月 SURETEX数星客赞助&quot;数星客户外&bull;2010第三届武功山国际帐篷节&quot;。2011年，数星客再度总冠名&quot;数星客&bull;2011第四届中国萍乡武功山国际帐篷节&quot;。</p>
<p>作为帐篷节的战略合作伙伴及总冠名商，数星客为本届帐篷节的所有工作人员、场地运营人员、应邀贵宾、志愿者、俱乐部领队，以及各大新闻媒体工作人员提供户外装备支持，并为赛事活动赞助大量奖金、奖品。 </p>
