<div class="mltw">
<a href="http://www.vibram.com/" target="_blank"><img src="http://static.8264.com/oldcms/moban/zt/2012-01-18-100YY/images/img5.jpg" /></a>
<p>Vibram是高性能橡胶鞋底的全球领导品牌，业务主要涵盖户外运动、工作、休闲、时尚、维修和矫形等鞋类市场。70多年来，著名的黄金八角形作为品牌标识已经成为质量、性能、安全、创新和设计的代名词。自二十世纪三十年代，Vibram首创了第一款为登山运动专门设计的橡胶鞋底，为登山运动带来革命性的影响。Vibram鞋底在提高安全和保护水平的期望中诞生，是持续致力于研发与创新的成果。产品遵循三大目标：确保最佳的性能、最高的舒适度和经久耐用的质量。</p>
</div>
<div class="mltw">
<a href="http://www.8264.com/65505.html" target="_blank"><img src="http://static.8264.com/oldcms/moban/zt/2012-01-18-100YY/images/img49.jpg" /></a>
<p>除了研发生产产品外，Vibram推崇户外探险精神和挑战极限的价值观，近年来加强积极参与越野跑运动。VibramTesterTeam（测试团队）的成员在极限环境中测试产品。自1998年,测试团队在一系列国际重要户外极限竞赛活动中穿着采用Vibram大底制作的专业户外越野跑鞋。至今，Vibram测试团队多达100人以上，分别进行10多个类别的产品测试：攀登、徒步、滑雪、越野、骑行、航海、高尔夫、钓鱼、狩猎、皮划艇等，在实际环境中检测这些运动的鞋类大底的使用情况。 </p>
</div>
<div class="mltw">
<a href="http://v.youku.com/v_show/id_XMzQxOTkwOTg4.html" target="_blank"><img src="http://static.8264.com/oldcms/moban/zt/2012-01-18-100YY/images/img40.jpg" /></a>
<p>从2007年开始，Vibram参与和赞助世界规模最大和最具影响力的环博朗峰超级越野跑（UTMB），Vibram测试团队越野跑运动员马可o欧默(MarcoOlmo)以58岁高龄连夺2006年和2007年两届环博朗峰超级越野跑(UTMB)冠军成为UTMB历史上的经典传奇故事。Vibram测试团队越野跑运动员马可o欧默(MarcoOlmo)也多次参加摩洛哥撒哈拉沙漠马拉松。</p>
</div>
<div class="mltw">
<a href="http://www.8264.com/viewnews-72880-page-1.html" target="_blank"><img src="http://static.8264.com/oldcms/moban/zt/2012-01-18-100YY/images/img48.jpg" /></a>
<p>来自27个国家的超过600位越野跑爱好者报名2012Vibram香港100公里超级越野赛。Vibram将招募5位越野跑爱好者组队参与目前中国最高难度的极限越野跑赛事，帮助这些平凡人去实现一个非凡的梦想，挑战累计4500米的上升下降高差和不可预见的疲劳、疼痛和困难，最终收获从城市跨越自然的极致体验，最终越过100公里终点线的目标。</p>
