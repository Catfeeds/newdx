<h2>COOLMAX&reg;面料</h2>
<div class="text-cont">
<p>为什么选择COOLMAX&reg;面料？原因很简单：保持干爽与舒适。无论是从事日常工作，或是体能运动，您的身体都无法避免这样一个事实：出汗。</p>
<p>出汗是人体自身保持凉爽的身理机制之一。英威达深刻理解酷爽舒适的重要性，COOLMAX&reg;面料由此应运而生。借助于特殊技术，这种面料就可以实现吸湿与增强干燥的功能。为此，您完全可以期望COOLMAX&reg;面料赋予您干适感受及源源不断的身体动力―― 这正是支持你从事日常工作或高强度运动的关键。</p>
<p>自1986年以来，COOLMAX&reg;面料已经帮助一些著名运动员在相关比赛领域中取得了令人瞩目的成绩。如今，对于渴望获得清凉柔软的高性能面料通史观众时尚趋势的人们来说，COOLMAX&reg;面料迎合了他们的需求。COOLMAX&reg;面料是运动爱好者们坚定而明智的选择。</p>
<p><strong>工作原理</strong></p>
<p><img src="http://static.8264.com/oldcms/moban/zt/2012coolmax/img/img-12.png"></p>
<p>COOLMAX&reg;面料帮助带走体表湿气。</p>
<p>COOLMAX&reg;面料可吸收并扩散湿气，从而加强蒸发干燥能力。</p>
<p>空气进入，使身体保持凉爽与干燥。</p>
<p><strong>有趣事实</strong></p>
<p>湿气管理 + 透气性能 = 舒适程度</p>
<p><strong>透气性能 ― COOLMAX&reg;</strong>面料的优异功能来源于本身的特殊结构。基于这种特殊结构，设计者才使得这种超级功能面料具有了无以伦比的功能：柔软质地、良好透气、超级吸湿。</p>
<p><strong>易于护理</strong> ― 用COOLMAX&reg;面料制作的服装可以采用机洗与干洗两种方式。但是，不允许使用织物柔软剂与氯漂白剂。由于款式而非面料的的缘故，有一些服装需要进行特殊处理。请仔细阅服装洗标。</p>
<p><strong>节约能源</strong> ― COOLMAX&reg;面料属于速干型，它可以极大地减少您的烘干费用。对于那些采用100% COOLMAX&reg;面料制作的服装，只要将湿衣服挂起来，即可快速晾干。其速度之快，足以令旅行者感到非常满意！</p>
<p><strong>全天候舒适</strong> ― 在寒冷的冬季，特别是在有氧健身运动中，COOLMAX&reg;面料将会构成一道有效屏障。它不仅可以吸收皮肤表面的湿冷汗液，将其排除于面料之外，形成有效蒸发功能。在保持身体干燥的同时，您也可以享受温暖。</p>
