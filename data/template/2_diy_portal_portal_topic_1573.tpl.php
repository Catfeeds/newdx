<div class="shengpingtitle">
<em>克诺特&#8226;耶格先生生平简介</em>
<span>1942.2.18 -- 2014.1.31</span>
</div>
<div class="shengpinginfo">
<ul>
<li>克诺特&#8226;耶格先生</li>
                <li>出生：1942年2月18日。德国林道（Lindau）</li>
                <li>逝世：2015年1月31日。香港</li>
<li>1975年，研发了"Big Pack"背包。</li>
<li>1976年，成立Big Pack户外用品公司。</li>
<li>1978年，背包加工外办给中国。</li>
<li>1980年，Big Pack公司的产品范围由背包增加到睡袋和羽服衣，德国研发中国制作。</li>
<li>1982年，Big Pack和R.G. Weismeier公司共同收购了Ess公司，并研发新产品。</li>
<li>1988年，将Ess公司卖给Atomic公司。</li>
<li>1990年，和Vaude、Jack Wolfskin、Salewa等主要户外用品公司发起成立德国户外用品协会，并于1990-1998连任8年德国户外协会主席。</li>
<li>1993年，为开拓中国市场，Big Pack和中国生产商建立了合资公司，在南京生产背包和睡袋。</li>
<li>1994年，作为共同发起人，在德国腓特烈斯哈芬创立了"欧洲户外用品展"。</li>
<li>2002年，把Big Pack卖给法国的Lafuma 公司。</li>
<li>2004年，在上海成立了Knut Jaeger营销发展公司，成为链接欧洲和中国的户外用品市场的桥梁，同时成为德国腓特烈斯哈芬展览有限公司、沃德（vaude）和Meindl在中国的营销顾问。</li>
<li>2004年，调研在中国举办专业户外用品展的可行性。</li>
<li>2005年，和南京签署合作协议，确定于2006年创办"亚洲户外用品展览会"。</li>
                <li>2006年，首届亚洲户外用品展成功在南京举办。</li>
<li>2006-2014年，连续成功举办9届"亚洲户外用品展览会"。</li>
<li>2009年，被南京市人民政府会展业办公室聘为南京会展专家咨询委员会成员。</li>
<li>2011年，在南京成功创办"亚洲自行车展览会"。</li>
<li>2011年，在南京发起成立"紫金亚洲户外论坛"，并开始筹备2011中国户外产业报告。</li>
<li>2012年，在2012亚洲户外展期间发布2011年度中国户外产业报告，受到行业欢迎。之后每年在亚洲户外展上发布上一年度户外产业报告。</li>
<li>2013年，在紫金亚洲户外论坛成员基础上发起成立"中国户外品牌联盟（COA）"。以COA推动中国户外市场健康平稳地发展。</li>
<li>2013年，联合南京宁菲公司，使得亚洲户外用品展通过了国际展览业协会（ufi）的认证，亚洲户外用品展成为中国第一个获得ufi认证的专业户外展。</li>
<li>2011-2014年，带领亚洲自行车展成功举办4届，亚洲自行车展成为中国最大的专业运动自行车贸易展。</li>
<li>2014年，克诺特&#8226;耶格先生全力推动亚洲通用航空展落地中国，首届展会将于2015年在珠海举办。</li>

</ul>
