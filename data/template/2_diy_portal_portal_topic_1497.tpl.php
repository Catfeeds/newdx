            	<div class="darktitle">VAUDE骑车去登山源由</div>
                <div class="imgpt5"><img src="http://static.8264.com/oldcms/moban/zt/2013vaude_qx/images/img1.jpg"/></div>
                <div class="info"><p>2009年，意大利登山家Diego Giovannini 和Franco 'Franz' Nicolini不依靠任何机械力量连攀了阿尔卑斯山脉地区所有4000米以的山峰（共计82座），且在每一座山峰中间的转移他们通过骑自行车来完成，他们成为完成这一壮举的第一对登山家。</p><p>由此，VAUDE发起了"骑车去登山"系列环保活动，VAUDE希望借助这种环保的攀登方式引发人们对于户外环保的思考。活动分为七一冰川、哈巴雪山、阿尔卑斯山三个站进行，七一冰川站已圆满结束，第二站"相约哈巴雪山最美的季节"即将启程，期待你的加入……</p></div>
            </div>
            <div class="r_570">
            	<div class="darktitle">活动介绍</div>
                <div class="imgpt5"><img src="http://static.8264.com/oldcms/moban/zt/2013vaude_qx/images/img2.jpg"/></div>
                <div class="info_r"><p>骑车去登山，是一种环保和充满想象力的攀登方式，你想尝试吗？一路上，凭借自己的意志和依靠自己的体力来接近山峰，虽然会更加辛苦，但是一次旅行可以把两项有意思的户外运动结合在一起，相信会很不同。户外运动发展到现在，离不开想象力，是时候该玩点新鲜的。</p>
<p>也许，你担心自己不行？没关系，咱们这是一次体验和训练，会提供一个良好的后勤保障。车坏了，骑不动都不用怕，只要你愿意出发。我们有专业的教练，在骑行和攀登两方面即使你是初试，也不用担心。相信这次旅程会让你认识许多志同道合的小伙伴，一次有意义的旅行，记录下有别于平常的自己。</p>
<p>本次的骑车去登山活动，由拥有超过80次登山经验的VAUDE绿色中国登山队提供后勤支持，VAUDE赞助运动员包一飞和方贤担任教练。每一位队员，都可以获得由VAUDE提供的装备，还可以获得由山力士提供的最新款头灯和山之厨提供的野外食品。</p>
