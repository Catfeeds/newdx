<p>KAILAS（凯乐石）寻岩中国从2012年至今，从广西到云南、从山东到湖北…20多个岩场，423条新线路，已将足迹印遍祖国大江南北。2015年，KAILAS将继续邀请岩界大咖为岩友开辟150－200条高质量攀岩路线。了解攀岩、感受攀岩、享受攀岩，寻岩中国你岂可错过。</p>
