<div class="actiontitle">
        <p>2012年至今，KAILAS寻岩中国从已经将足迹印在了祖国大江南北。从广西到云南、从山东到湖北……20多个岩场，423条新线路，三年来，KAILAS寻岩团队专注于在全国各地开发攀岩路线、推广攀岩文化，前所未有的线路增长速度，也直接推动着中国攀岩运动的发展。</p>
        <p>2015年，依旧由KAILAS攀登队员谢卫成、赵忠军牵头的中国民间攀登中坚力量再次集结，寻岩中国团队即将强势回归。张勇、邱江、Marcos（巴西）、张晓柳、苏峰等中国岩届大咖将在四个寻岩站点为岩友开辟总计150－200条高质量攀岩路线，其中会包括部分多段结组线路。</p>
        <p>新一轮的活动中，团队将以开发线路、分享经验、交流技术、传播理念为已任。除了开辟优质的攀岩路线，他们也将沿袭过去三年得到众多好评的岩友会活动，以此为平台分享攀岩技术、安全、环保等理念，影响更多人加入到攀岩运动中。</p>
