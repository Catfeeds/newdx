<p style="text-indent:2em;">滑雪登山在英文中叫“Skimountaineering”，法语中称为'SkiAlpine'。所谓滑雪登山，就是使用滑雪器材登上雪山并一路滑下来。在欧美、特别是阿尔卑斯、比利牛斯山区周边国家已经开展得十分广泛。滑雪登山运动是包括了极限滑雪、高山速降、越野滑雪、和雪山攀登技术的综合性冬季户外运动项目。要求参与者拥有极好的体能和高超的滑雪和雪山攀登技术。</p>
<p><img src="http://static.8264.com/oldcms/moban/zt/2012gangshika/img/img-2.jpg"></p>
<p style="text-indent:2em;">滑雪登山，是项近乎“疯狂”的运动。登山者都是有些“疯狂”的一类人了，而玩滑雪登山的人，更是“疯狂”</p>
</div>
<div class="area1_1">
<p><img src="http://static.8264.com/oldcms/moban/zt/2012gangshika/img/img-12.jpg"></p>
<p style="text-indent:2em;">中的“疯狂”者。他们要穿着沉重的滑雪登山两用靴和攀登冰爪，背着长而重的滑雪板和其他所有登山和滑雪都必要的装备，可能需要运用所有的攀登技巧，顶着可能更强烈的暴风雪爬上冬季更具挑战的巅峰；然后，找到令人望而生畏的陡雪坡，冒着雪崩和坠落断壁的风险，风驰电掣般临风而下。　滑雪登山着实是一项技术、体质和心理素质要求较高的运动。</p>
<p style="text-indent:2em;">现代滑雪登山竞赛包括垂直攀登，个人越野、团队越野、团体接力赛等项目。垂直攀登要求运动员使用滑雪板在最短的时间内在陡峭的雪坡上向上攀登高差</p>
</div>
<div class="area1_1" style="margin-right:0;">
<p>一千米左右的距离。而越野赛则要求运动员利用滑雪板连续攀登和快速滑下，甚至要求运动员携带滑雪装备，利用绳索、冰爪等雪山攀登装备登上陡峭的山峰然后滑雪下降。滑雪登山竞赛必须使用专用的滑雪板脱离器、海豹皮摩擦条( 贴在滑雪板下面，用于攀登时防止下滑，现已用人造纤维材料代替了珍贵的动物皮毛 )、雪崩示踪器、雪铲、不少于 20升的背包和防寒服装等装备。<a href="http://www.8264.com/portal.php?mod=view&amp;aid=66572" class="more" target="_blank">详细 >></a></p>
