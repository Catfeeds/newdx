<div class="area3_2">
<p>岗什卡雪山（又称冷龙岭）是青海境内横卧在祁连山体上的一条银色的雪龙，其主峰海拔5254.5米，是这座雄伟壮美山体上的一颗瑰丽的明珠。因为它的壮美秀丽，所以总会令爱山的人怦然心动，激情难抑，总有亲近它的冲动，拜望它的欲望。心动就会促成行动，2007年6月24日12时55分，我同张勇两人实现了登顶的愿望。现将有关登山情况做一回顾，供广大山友参考。</p>
</div>
<div class="area3_3">
<p>岗什卡雪峰座落在青海省门源县境内皇城蒙古族乡东滩村北部，交通方便，后援保障便利，攀登难度不大。它是距离青海省会西宁最近的第一高峰，所以，对登山爱好者的吸引力较大。该峰山体延西北―东南走向，峰顶终年积雪，山体高大，雄浑厚重。远望群峰，层峦叠嶂，气势恢宏。山体有现代冰川发育，冰川总面积达81平方公里，年平均气温在零度以下</p>
</div>
<div class="area3_4">
<p>它集冰、雪、碎石少量岩壁为一体，明暗冰裂缝分布在冰川之上，为登山者提供了充分施展手足的空间。是业余登山爱好者实现登顶雪山冰峰的理想选择。</p>
<p>岗什卡雪峰又是当地居民心中的一座神山，祁连山东段的最高峰。由于其特殊的山体走向和位置，顶峰周边在傍晚夕阳西下时，经常出现迷人的冷龙夕照美景。在霞光的映衬下的岗什卡雪峰显得格外的庄严和壮丽。山的神奇，山的美丽，让人不由产生敬畏之情。站在那里仰望它，就足以让人产生美丽的震撼和无限的暇想。<a href="http://www.8264.com/portal.php?mod=view&amp;aid=24083" class="more" target="_blank">[详细] </a></p>
</div>
