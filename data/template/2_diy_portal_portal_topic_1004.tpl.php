        <td class="activity_desc_2"><b style="color:#AA2415">一．活动地点：</b>南京户外展<br><br>




<b style="color:#AA2415">二．活动人员：</b>户外驴友及从业者<br><br>




<dl><dt><b style="color:#AA2415">三．主办方：</b><dd>8264</dd>
            <dd>喜力高背囊<br></dd></dt><br />

    <dt>&nbsp;&nbsp;&nbsp;&nbsp;<b style="color:#AA2415">协办方：</b><dd>南京户外展组委会</dd></dt></dl>




<b style="color:#AA2415">四．征集范围：</b><br />07年南京户外展期间的拍摄作品<br><br>



<b style="color:#AA2415">五．活动时间：</b><br>
  1、活动的参与期：2007年8月20日-2007年8月31日<br>
              2、活动的评选期：2007年9月1日-2007年9月5日<br>
              3、活动结果公布：2007年9月6日<br>
              4、活动奖品的发放：2007年9月6日-2007年9月10日<br><br>




<b style="color:#AA2415">六、活动流程</b><br>



A．8月23日--8月26日展会期间，8264展台为此次摄影比赛预留一台电脑供参赛人员现场上传照片专用，现场上传者会得到由南京户外展组委会提供的小礼品一份。<br><br />



<span style="color:#FF0000;">要求：1 不得转载、盗用其他人的摄影作品<br>

     &nbsp;&nbsp;&nbsp;&nbsp; &nbsp;2 必须以07年南京展为主题<br><br /></span>



B．8月27日&#8212;8月30日展会结束后，由8264工作人员、喜力高公司、8264论坛行行摄摄斑竹、南京户外展组委会组成评审团，对进入决赛的作品进行最终评审。最终决出一、二、三等奖，获奖名单将于9月1日在8264驴友论坛中公布。并按照获奖者提供的有效联系方式（电话或者电子信箱等）当日通知获奖者。<br><br />




C．2007年9月1日-2007年9月4日活动奖品发放。<br><br />


<span style="color:#FF0000;">活动奖品如下：<br>


一等奖2名奖品：喜力高675元专业摄影背包一个。198元喜力高速干衣一件。<br>

二等奖3名奖品：喜力高235元专业摄影包一个。198元喜力高速干衣一件。<br>

三等奖5名奖品：喜力高175元专业摄影包一个。59元喜力高T恤一件。<br>




积极参与奖20名奖品：喜力高58元腰包一个。59元喜力高T恤一件。<br>


奖品（摄影包、男女速干衣及T恤）全部由喜力高公司提供。<br></span>
