$(function(){
var num = $(".rwlistgun span").size();
var li_w = $(".rwlistgun span").width()+36;
var w = li_w * num;
$(".rwlistgun").width(w);
var i=1;
$(".rwlistgun span").mouseenter(function(){	
var index = $(this).index();
$(this).addClass("ts").siblings().removeClass("ts");
$(".rwimglist li").eq(index).fadeIn().siblings().hide(0);
});
$(".rwlarrow").click(function(){
if(i>1){
var l = $(".rwlistgun").position().left + li_w;
$(".rwlistgun").animate({
left:l,
},"slow");
i--;
}
});
$(".rwrarrow").click(function(){
if(i< num-3 ){
$(".rwlistgun").animate({
left: -i * li_w,
},"slow");
i++;
}
});		
});
