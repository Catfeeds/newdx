<h2>尼克&bull;泰勒（NICK TAYLOR）&mdash; 美国尾波冲浪运动员</h2>
<p>22岁的阳光男孩尼克&bull;泰勒有着6年尾波冲浪的经历，他健康的古铜肤色，以及对于这项运动令人难以置信的驾驭能力使他成为尾波冲浪界不折不扣的&quot;金童&quot;。</p>
<p> 尼克来自美国佛罗里达州的Anna Maria 岛，和所有岛上的居民一样，从小赤足嬉戏水边。无论是牵引滑水的人所产生的湍流，还是船舶或者摩托艇的尾流，Nick都能轻松驾驭，还能详细解构其动作要领。尼克目前的赞助商包括：Nike 6.0, O'Neill, Oakley, Integrity Wakeskates 和 The Republic。 </p>
