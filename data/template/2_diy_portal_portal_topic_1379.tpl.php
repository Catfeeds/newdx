<h2>卡伦&bull;卡珀斯（CURREN CAPLES） &mdash; 美国冲浪/滑板潜力之星</h2>
<p>今年15岁的卡伦&bull;卡珀斯对滑板爱好者来说都不陌生，他4岁就爱上滑板的感觉，6岁开始参加比赛，11岁时陆续赢得加州各类滑板比赛的冠军。现在他拥有Vans、Flip、Hurley、红牛等知名品牌的赞助。卡伦最喜欢去的国家就是中国，去年，他就曾作为嘉宾来到北京表演。当然，作为前职业冲浪手伊万&bull;卡珀斯的儿子，卡伦遗传到了大把的冲浪基因，他经常随父亲一起在世界上追随大浪。今年的钱塘江国际冲浪挑战赛，我们将邀请90后滑手/冲浪手卡伦来到中国挑战钱塘江大潮，并在Silver Dragon滑板年终总决赛上一展身手！ </p>
