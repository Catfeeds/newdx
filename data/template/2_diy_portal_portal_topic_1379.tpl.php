<h2>科林&bull;哈林顿(COLLIN HARRINGTON) &mdash; 美国尾波冲浪运动员</h2>
<p>科林&bull;哈林顿（Collin Harrington）来自佛罗里达州的奥兰多市，有着12年丰富的尾波冲浪经验，除了尾波板，科林擅长一切与水有关的运动，包括长板冲浪和潜水。科林的创造力同时从水上延伸到影视制作，他主管的Liquid Force Films影视公司，成功制作发行了尾波冲浪电影&quot;Box of Fun&quot;。 </p>
