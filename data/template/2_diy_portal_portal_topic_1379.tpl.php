<h2>克洛伊&bull;卡尔蒙（Chloe Calmon）&mdash; 巴西冲浪宝贝</h2>
<p>1994年出生的冲浪美少女克洛伊是著名长板选手Miguel Calmon的爱女，这个年轻的巴西姑娘继承了父亲的长板事业。Chloe开始接触长板的时候只有十一岁，一年之后她开始参加自己人生中首次国家级冲浪比赛，然后晋级国际级冲浪大赛。尽管她的竞争对手都是比自己年长的选手，但是其凭藉自身的努力一直走了下去，在众多赛事中获得不俗成绩：</p>
<p>ASP世界排名第九</p>
<p> Petrobras巡回赛巴西站冠军</p>
<p> 里约热内卢18岁以下冲浪赛冠军（与男选手同场竞技）</p>
<p> 巴西职业冲浪赛亚军</p>
<p> 巴西短板冲浪排名第三</p>
