<h2>赛尔金&bull;劳斯（Sergio Laus）D　吉尼斯冲浪王</h2>
<p>劳斯于2009年5月在亚马逊河创造了一项新的内河冲浪世界记录：33.15分钟内，连续冲浪总距离达到10.1公里。平均行进速度达到每小时27英里，浪高达到4米。在此之前，这一纪录由他个人在2007年创造，连续冲浪总距离为10公里。</p>
<p> 他曾多次在亚马逊河冲浪，２００８年８月他和队友Jorge Pacelli应邀试冲钱江潮，劳斯感叹不已：&quot;在亚马逊就像探险，两边都是茂密的丛林，不时有动物经过；这儿完全不一样&mdash;&mdash;两边都是高大的建筑，我们像在城市中穿行，太酷了！&quot;</p>
