<div class="red_tit m_t_10px"><span class="fr_zt"></span>冲浪及板类运动文化展</div>
<div class="show_list_box_2">
<a href="#" target="_blank"><img src="http://static.8264.com/oldcms/moban/zt/2011surfing/img/whz.jpg" /></a>
<p>自上世纪60年代美国加州年轻人将冲浪动作改编成陆地滑板动作以来，板类运动逐渐囊括了冲浪、滑板、单板三项极限运动。板类运动的历史，是体育运动发展的历史，也是年轻人不断挑战自我的历史。本次冲浪及板类运动文化展，将在杭州城市规划馆展示厅展出40幅有关冲浪、滑板、单板运动的珍贵影像资料，为观众呈现最具视觉冲击力的图片和文字。</p>
<p>完成了北京、上海巡展的第二届SDA艺术滑板创作展也将现身展览现场，SDA一年一度在中国多个城市巡回展出，特邀国际艺术家和本土艺术家对实用艺术的概念做出新的阐释。这一无与伦比的活动不仅仅有艺术展览，还会有现场绘画、滑板表演和现场音乐。SDA 2011巡回展站点的同期特别活动更是让每次SDA展都变成当地的活动亮点。</p>
<p>今年，沸点滑板 SDA 2011将再次因地制宜地布展，展出来自三大洲的众多艺术家的作品，他们的每个作品都融化了滑板和艺术的界限，探索了如何用独特的方式让人们来感受滑板这种全新生活方式，同时还用艺术的形式吸引更多的观众。</p>
