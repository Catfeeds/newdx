<h2>罗伯特&bull;卫弗（Robert &quot;Wingnut&quot; Weaver）&mdash;好莱坞冲浪电影明星</h2>
<p>继2010年在冲浪板上表演了&quot;罗氏倒立&quot;后，人称&quot;Wingnut&quot;的冲浪元老级人物罗伯特&bull;卫弗将再次造访钱塘江。Wingnut不仅是一名优秀的冲浪手，更是一位电影明星。他灿烂的笑容和阳光的形象的出现在无数冲浪影像产品上。他在多部布鲁斯&bull;布朗的冲浪电影里担任主角，与另一名冲浪手Pat O'Connell合作出演了&quot;无尽的夏日&quot; 续集（The Endless Summer II），&quot;无尽的夏日&quot;被誉为世界十大经典体育电影之一，是冲浪影片中的代表作。</p>
<p>在大银幕上获得成功的同时，他还是诸多好莱坞明星指定的冲浪教练，好莱坞影帝尼古拉斯&bull;凯奇就是他的学员之一。 </p>
