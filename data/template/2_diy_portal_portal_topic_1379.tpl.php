<h2>玛丽&bull;奥斯本（Mary Osborne）―全能冲浪美女</h2>
<p>在加州艳阳下长大的玛丽&bull;奥斯本从14岁开始冲浪，在MTV真人秀&quot;冲浪女孩&quot;中获得长板冠军而为人们熟悉。如今玛丽不仅是一名职业冲浪手，而且身兼模特，节目主持和商人，是冲浪届少有的全能美女。</p>
<p> 2002年，玛丽在颇受欢迎的电视节目&quot;冲浪女孩&quot;中获得冠军。</p>
<p> 2003年，在青少年的评选中她当选最佳女运动员。玛丽还是Action Girl评选出的第一位&quot;年度极限女孩&quot;。</p>
<p> 2005年，玛丽著书《冲浪姊妹》，为女性冲浪指南。</p>
<p> 2007年，玛丽发起了以自己名字命名的冲浪训练营和瑜伽课程。</p>
<p> 2008年，玛丽开始经营自己的公司Solymar Tan，已经开到三家分店。</p>
<p> 她为福克斯体育频道主持体育节目，常现身于诸多有限电视节目中。2010年第一次成功挑战钱江潮后，玛丽对杭州留下了极为深刻的印象，能第二次成为钱塘江的&quot;冲浪大使&quot;，玛丽兴奋不已。</p>
</div>
