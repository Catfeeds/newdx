<h2>埃瓦拉多&bull;特谢拉（Everaldo Teixeira）&mdash; 巴西大浪专家</h2>
<p>巴西职业自由冲浪手埃瓦拉多，朋友亲密地叫他&quot;帕托（Pato）&quot;，有过在夏威夷的毛伊岛挑战高达70英尺，约21米巨浪的经历，无比的勇气使得帕托面对任何危险状况都能镇定自如，他不断挑战自己的极限，足迹遍布世界上几乎所有的冲浪圣地。冲浪专业杂志上时有他的照片，他始终和编辑，摄影师们保持着良好的关系。他的上一次壮举是2008年在加州Ghost Tree和传奇人物Carlos Burle合作，帕托和Burle协作完成了一个大浪动作，该照片成为冲浪第一杂志《Surfer》的封面，成为出现在这个杂志封面上的第一位巴西冲浪手。 </p>
